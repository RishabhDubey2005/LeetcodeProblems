n = 0

# While Loops
while n < 5:
    print(n)
    n += 1
    
print()

# For Loops
for i in range(5): # 5 is NOT included in the iteration 0 -> 5 - 1
    print(i)

print()
    
# For Looping from 1 = 2 to 1 = 5
# Go from 0 -> 6 - 1
for i in range(2,6):
    print(i)
    
print()

# For Loop Using Reverse Iteration:
# 6: Starting Index, 2(-1): Final index, -1: How much you want to increment/decrement by
for i in range(6, 2, -1):
    print(i)

print()